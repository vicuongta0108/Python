print('Basic way: i in [0, 1, 2, 3, 4, 5]')
for i in [0, 1, 2, 3, 4, 5]:
    print(i)

print('\nAnother way to loop: i in range(6)')
for i in range(6):
    print(i)

print('\nPrinting each name in a list of names')
names = ['Harry', 'Ron', 'Hermione']
for name in names:
    print(name)

print("\nPrinting each character in name")
name = 'Harry'
for chr in name:
    print(chr)